package com.parcelcheck.parcelchecklib;

import java.math.BigDecimal;
import java.math.BigInteger;

public class PrimitiveFilledObject {

    String string;

    int integer;
    Integer classInt;

    boolean primitiveBoolean;
    Boolean classBoolean;

    double primitiveDouble;
    Double classDouble;

    float primitiveFloat;
    Float classFloat;

    long primitiveLong;
    Long classLong;

    BigDecimal bigDecimal;
    BigInteger bigInteger;

    byte primitiveByte;
    Byte classByte;

    short primitiveShort;
    Short classShort;

    public int getInteger() {
        return integer;
    }

    public Integer getClassInt() {
        return classInt;
    }

    public String getString() {
        return string;
    }

    public boolean isPrimitiveBoolean() {
        return primitiveBoolean;
    }

    public Boolean getClassBoolean() {
        return classBoolean;
    }

    public double getPrimitiveDouble() {
        return primitiveDouble;
    }

    public Double getClassDouble() {
        return classDouble;
    }

    public float getPrimitiveFloat() {
        return primitiveFloat;
    }

    public Float getClassFloat() {
        return classFloat;
    }

    public long getPrimitiveLong() {
        return primitiveLong;
    }

    public Long getClassLong() {
        return classLong;
    }

    public BigDecimal getBigDecimal() {
        return bigDecimal;
    }

    public BigInteger getBigInteger() {
        return bigInteger;
    }

    public byte getPrimitiveByte() {
        return primitiveByte;
    }

    public Byte getClassByte() {
        return classByte;
    }

    public short getPrimitiveShort() {
        return primitiveShort;
    }

    public Short getClassShort() {
        return classShort;
    }
}
